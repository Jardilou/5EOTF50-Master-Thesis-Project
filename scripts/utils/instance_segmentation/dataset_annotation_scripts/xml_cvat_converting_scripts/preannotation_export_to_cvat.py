"""
================================================================================
Script: YOLO to CVAT XML Annotation Exporter
================================================================================

Description:
    A script to convert YOLO-format instance segmentation labels into the CVAT 
    (Computer Vision Annotation Tool) 1.1 XML format. This conversion allows 
    pre-annotated datasets generated by models like SAM or YOLO to be directly 
    imported into CVAT for manual review, Quality Assurance (QA), and refinement.

    Key Functions:
    1. XML Meta-Structure Initialization: Generates the required CVAT XML header, 
       including task definitions and class label color-coding mappings.
    2. Coordinate Denormalization: Reads source image dimensions to convert 
       normalized YOLO polygon coordinates [0.0 - 1.0] back to absolute pixel 
       coordinates required by CVAT.
    3. Format Conversion: Translates text-based YOLO class IDs and coordinate 
       arrays into CVAT-compatible `<polygon>` XML nodes.
    4. XML Serialization: Outputs a structured, pretty-printed `.xml` file 
       ready for direct upload to the CVAT web interface.

Author:
    Louis Jardinet
    Universiti Teknologi Petronas

Date:
    January 2026 - June 2026

Dependencies:
    - Python standard libraries: os, xml.etree.ElementTree, xml.dom.minidom
    - External packages: opencv-python (cv2)

Inputs:
    - img_dir: Directory containing the source images (required to extract 
      absolute pixel dimensions).
    - txt_dir: Directory containing the YOLO .txt polygon label files.

Outputs:
    - output_xml_path: The file path where the generated CVAT XML file 
      will be saved.
================================================================================
"""

import os
import cv2
import xml.etree.ElementTree as ET
from xml.dom import minidom

def generate_cvat_xml(img_dir, txt_dir, output_xml_path):
    print("Generating CVAT XML structure...")
    root = ET.Element("annotations")
    ET.SubElement(root, "version").text = "1.1"

    meta = ET.SubElement(root, "meta")
    task = ET.SubElement(meta, "task")
    labels = ET.SubElement(task, "labels")

    # Define your exact classes here
    class_names = {0: "measurable_fish", 1: "unmeasurable_fish"}


    for class_id, class_name in class_names.items():
        label = ET.SubElement(labels, "label")
        ET.SubElement(label, "name").text = class_name
        ET.SubElement(label, "color").text = "#33ddff" if class_id == 0 else "#ff3333" # Visual distinction

    image_files = [f for f in os.listdir(img_dir) if f.endswith(('.jpg', '.png'))]
    image_files.sort()

    processed_count = 0

    for img_id, img_name in enumerate(image_files):
        img_path = os.path.join(img_dir, img_name)
        txt_path = os.path.join(txt_dir, img_name.replace('.jpg', '.txt'))

        # We must open the image to get absolute pixel dimensions for CVAT
        img = cv2.imread(img_path)
        if img is None: continue
        h, w = img.shape[:2]

        image_elem = ET.SubElement(root, "image", id=str(img_id), name=img_name, width=str(w), height=str(h))

        if os.path.exists(txt_path):
            with open(txt_path, 'r') as f:
                for line in f:
                    parts = line.strip().split()
                    if not parts: continue

                    class_id = int(parts[0])
                    label_name = class_names.get(class_id, "unknown")

                    # Extract normalized coordinates: x1 y1 x2 y2 ...
                    coords = [float(p) for p in parts[1:]]

                    # Convert normalized [0.0-1.0] back to absolute pixels
                    points_str = []
                    for i in range(0, len(coords), 2):
                        px = coords[i] * w
                        py = coords[i+1] * h
                        points_str.append(f"{px:.2f},{py:.2f}")

                    # Write the polygon node for CVAT
                    ET.SubElement(image_elem, "polygon", label=label_name, source="auto", points=";".join(points_str), z_order="0")
            
            processed_count += 1

    # Format the XML to be human-readable and acceptable by CVAT
    xml_str = minidom.parseString(ET.tostring(root)).toprettyxml(indent="  ")
    with open(output_xml_path, "w") as f:
        f.write(xml_str)

    print("-" * 50)
    print(f"Success! Generated {output_xml_path}")
    print(f"Packed annotations for {processed_count} images.")

